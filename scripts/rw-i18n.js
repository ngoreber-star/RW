        let _lang = (() => { try { return localStorage.getItem('rw_lang') || 'es'; } catch(e) { return 'es'; } })();

        function t(key, ...args) {
            const dict = T[_lang] || T.es;
            let str = (dict[key] !== undefined ? dict[key] : (T.es[key] !== undefined ? T.es[key] : key));
            if (args.length) args.forEach((a, i) => { str = str.replace(new RegExp('\\{' + i + '\\}', 'g'), a); });
            return str;
        }

        function setLang(lang) {
            if (!T[lang]) return;
            _lang = lang;
            try { localStorage.setItem('rw_lang', lang); } catch(e) {}
            document.documentElement.setAttribute('lang', lang);
            applyDOM();
            document.querySelectorAll('.rw-lang-btn').forEach(btn => {
                btn.classList.toggle('font-bold', btn.dataset.lang === _lang);
                btn.classList.toggle('text-primary-600', btn.dataset.lang === _lang);
                btn.classList.toggle('text-gray-500', btn.dataset.lang !== _lang);
            });
            if (window.ui && window.AppState?.currentPage) {
                try { window.AppController.renderNavigation(); } catch(e) {}
                try { window.ui.navigateTo(window.AppState.currentPage); } catch(e) {}
            }
        }

        function applyDOM(root) {
            const scope = root || document;
            scope.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.getAttribute('data-i18n')); });
            scope.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = t(el.getAttribute('data-i18n-html')); });
            scope.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.getAttribute('data-i18n-placeholder')); });
            scope.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.getAttribute('data-i18n-title')); });
            scope.querySelectorAll('[data-i18n-value]').forEach(el => { el.value = t(el.getAttribute('data-i18n-value')); });
            if (!root) {
                const langLabel = document.getElementById('langLabel');
                if (langLabel) langLabel.textContent = (_lang || 'es').toUpperCase();
            }
        }

        function translateContainer(root) {
            if (!root) root = document.body;
            applyDOM(root);
            const dict = T[_lang] || T.es;
            if (!window.__I18N_ES_MAP) {
                window.__I18N_ES_MAP = {};
                for (const [k, v] of Object.entries(T.es)) {
                    if (v && typeof v === 'string') window.__I18N_ES_MAP[v.trim()] = k;
                }
            }
            const esMap = window.__I18N_ES_MAP;
            // Sort keys by length descending for substring replacement
            const entries = Object.entries(esMap)
                .filter(([esText, key]) => esText.length >= 2 && !esText.includes('{0}') && !esText.includes('{1}'))
                .sort((a, b) => b[0].length - a[0].length);

            const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
            let node;
            while (node = walker.nextNode()) {
                let text = node.textContent;
                let changed = false;
                for (const [esText, key] of entries) {
                    if (text.includes(esText)) {
                        const translated = dict[key];
                        if (translated && translated !== esText) {
                            text = text.split(esText).join(translated);
                            changed = true;
                        }
                    }
                }
                if (changed) node.textContent = text;
            }

            // Translate common attributes
            const attrKeys = { placeholder: [], title: [], value: [] };
            for (const [esText, key] of entries) {
                if (esText.length >= 3) {
                    attrKeys.placeholder.push([esText, key]);
                    attrKeys.title.push([esText, key]);
                    attrKeys.value.push([esText, key]);
                }
            }
            root.querySelectorAll('[placeholder]').forEach(el => {
                let v = el.placeholder;
                for (const [esText, key] of attrKeys.placeholder) {
                    if (v.includes(esText)) {
                        const translated = dict[key];
                        if (translated) v = v.split(esText).join(translated);
                    }
                }
                if (v !== el.placeholder) el.placeholder = v;
            });
            root.querySelectorAll('[title]').forEach(el => {
                let v = el.title;
                for (const [esText, key] of attrKeys.title) {
                    if (v.includes(esText)) {
                        const translated = dict[key];
                        if (translated) v = v.split(esText).join(translated);
                    }
                }
                if (v !== el.title) el.title = v;
            });
            root.querySelectorAll('[value]').forEach(el => {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    let v = el.value;
                    for (const [esText, key] of attrKeys.value) {
                        if (v.includes(esText)) {
                            const translated = dict[key];
                            if (translated) v = v.split(esText).join(translated);
                        }
                    }
                    if (v !== el.value) el.value = v;
                }
            });
        }

        function getLang() { return _lang; }

